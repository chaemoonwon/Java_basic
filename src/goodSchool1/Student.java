package goodSchool1;

public class Student {
    String stName;
    String stNum;
    String major;
    String sub;
    int kor;
    int math;

    public Student(String stName, String stNum, String major, String sub, int kor, int math) {
        this.stName = stName;
        this.stNum = stNum;
        this.major = major;
        this.sub = sub;
        this.kor = kor;
        this.math = math;
    }
}
